package usc.app.coinmarket.objetos;

public class Objeto_noticias {
    private String nombreN;
    private String autorN;
    private String imagenN;
    private String tiempoN;
    private String urlN;
    public Objeto_noticias() {
    }

    public String getUrlN() {
        return urlN;
    }

    public void setUrlN(String urlN) {
        this.urlN = urlN;
    }

    public String getAutorN() {
        return autorN;
    }

    public void setAutorN(String autorN) {
        this.autorN = autorN;
    }

    public String getNombreN() {
        return nombreN;
    }

    public void setNombreN(String nombreN) {
        this.nombreN = nombreN;
    }

    public String getImagenN() {
        return imagenN;
    }

    public void setImagenN(String imagenN) {
        this.imagenN = imagenN;
    }

    public String getTiempoN() {
        return tiempoN;
    }

    public void setTiempoN(String tiempoN) {
        this.tiempoN = tiempoN;
    }
}
