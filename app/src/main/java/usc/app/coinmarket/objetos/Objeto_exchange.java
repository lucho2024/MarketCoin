package usc.app.coinmarket.objetos;

public class Objeto_exchange {
    private String nombre_E;
    private String pais_E;
    private String enlace_E;
    private String imagen_E;
    private Double volumen_E;


    public Objeto_exchange() {

    }

    public String getNombre_E() {
        return nombre_E;
    }

    public void setNombre_E(String nombre_E) {
        this.nombre_E = nombre_E;
    }

    public String getPais_E() {
        return pais_E;
    }

    public void setPais_E(String pais_E) {
        this.pais_E = pais_E;
    }

    public String getEnlace_E() {
        return enlace_E;
    }

    public void setEnlace_E(String enlace_E) {
        this.enlace_E = enlace_E;
    }

    public String getImagen_E() {
        return imagen_E;
    }

    public void setImagen_E(String imagen_E) {
        this.imagen_E = imagen_E;
    }

    public Double getVolumen_E() {
        return volumen_E;
    }

    public void setVolumen_E(Double volumen_E) {
        this.volumen_E = volumen_E;
    }
}
