package usc.app.coinmarket.objetos;

public class Objeto_buscador {

    private String nombreBuscador;
    private Double precioBuscador;
    private String fotoBuscador;
    private String idBuscador;
    private Double Mbuscador;


    public Objeto_buscador() {
    }

    public String getNombreBuscador() {
        return nombreBuscador;
    }

    public void setNombreBuscador(String nombreBuscador) {
        this.nombreBuscador = nombreBuscador;
    }

    public Double getPrecioBuscador() {
        return precioBuscador;
    }

    public void setPrecioBuscador(Double precioBuscador) {
        this.precioBuscador = precioBuscador;
    }

    public String getFotoBuscador() {
        return fotoBuscador;
    }

    public void setFotoBuscador(String fotoBuscador) {
        this.fotoBuscador = fotoBuscador;
    }

    public String getIdBuscador() {
        return idBuscador;
    }

    public void setIdBuscador(String idBuscador) {
        this.idBuscador = idBuscador;
    }

    public Double getMbuscador() {
        return Mbuscador;
    }

    public void setMbuscador(Double mbuscador) {
        Mbuscador = mbuscador;
    }
}
